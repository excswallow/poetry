package com.myth.shishi.entity;

public class Yun
{

    private int tone;

    private String glys;

    private String section_desc;

    private String tone_name;

    public int getTone()
    {
        return tone;
    }

    public void setTone(int tone)
    {
        this.tone = tone;
    }

    public String getGlys()
    {
        return glys;
    }

    public void setGlys(String glys)
    {
        this.glys = glys;
    }

    public String getTone_name()
    {
        return tone_name;
    }

    public void setTone_name(String tone_name)
    {
        this.tone_name = tone_name;
    }

    public String getSection_desc()
    {
        return section_desc;
    }

    public void setSection_desc(String section_desc)
    {
        this.section_desc = section_desc;
    }
}
